"""
LLM-as-a-judge プロンプト集 (Phase 2)

Phase 2 で導入する4種類の意味的判定プロンプトを集約。
各プロンプトは:
- 役割の明示 (中高生向け数学教育の専門家)
- 評価観点の具体化
- 厳密な JSON 形式での出力指示
- 1〜5 のスコア基準の説明

判定対象テストとの対応:
- ACCURACY_JUDGE_PROMPT          : AI-A-001〜004
- PREREQUISITE_JUDGE_PROMPT      : AI-S-002 (救済型)
- INPUT_REFERENCE_JUDGE_PROMPT   : AI-Q-002 (個別再判定型)
- CONSISTENCY_JUDGE_PROMPT       : AI-C-003 (同趣旨型)

実装上の注意:
プロンプト内に JSON 出力例 ({ "score": ... } 等) を含むため、
str.format() を使うと JSON の波括弧が変数プレースホルダと誤認識される。
このため string.Template (${name} 形式) を採用。${} と JSON の {} が衝突しない。

テンプレート定数の型はすべて string.Template 。
helpers/llm_judge.py 側では prompt_template.substitute(**variables) で展開する。
"""

from string import Template


# 判定 AI に共通で求める出力形式の説明 (各プロンプトの末尾に連結)
# 注: 内部に $ を含めない (Template に渡す前に + 連結するため)
_OUTPUT_FORMAT_INSTRUCTION = """
以下の JSON 形式のみで応答してください。コードブロック (```json ... ```) や前置き、解説は一切含めないでください。

{
  "score": 1-5の整数,
  "passed": true/false,
  "reasoning": "判定理由を具体的に200字以内で記述"
}

スコア基準:
- 5: 期待を超える品質
- 4: 期待通り (合格)
- 3: 許容範囲だが改善余地あり (合格)
- 2: 不十分 (不合格)
- 1: 明らかに問題あり (不合格)

passed フィールドは score >= 3 で true、それ以外は false としてください。
""".strip()


# ===== AI-A-001〜004: 正確性 (引き締め型) =====
ACCURACY_JUDGE_PROMPT = Template("""\
あなたは中高生向け数学教育の専門家として、学習レコメンドの推薦理由を評価する役割です。

以下の入力情報と推薦結果を見て、推薦理由が「入力された生徒の状況 (学年・理解済み・苦手) を踏まえて、教育的に妥当な内容か」を評価してください。

【入力情報】
- 学年: ${grade}
- 理解済み単元: ${understood}
- 苦手単元: ${weak}

【推薦された単元】
${recommended_units}

【評価対象の推薦理由】
${reason}

【期待される観点 (参考、必須ではない)】
${expected_keywords}

【評価の着眼点】
- 推薦理由が、生徒の学習文脈 (学年・既習内容・つまずき) と整合しているか
- 単に表面的にキーワードを羅列しただけでなく、教育的に妥当な順序や繋がりを示しているか
- 中高生に分かりやすい言葉で書かれているか
- 期待される観点はあくまで参考。明示的に含まれていなくても、趣旨が伝われば合格判定して良い

""" + _OUTPUT_FORMAT_INSTRUCTION)


# ===== AI-S-002: 前提関係 (救済型) =====
PREREQUISITE_JUDGE_PROMPT = Template("""\
あなたは中高生向け数学教育の専門家として、推薦理由内の単元言及が「教育的に妥当な前提関係を述べているか」を評価する役割です。

【入力情報】
- 学年: ${grade}
- 理解済み単元: ${understood}
- 苦手単元: ${weak}

【推薦された単元】
- ID: ${recommended_unit_id}
- 名前: ${recommended_unit_name}
- 学年区分: ${recommended_unit_grade}

【ルールベース判定で「前提チェーンに含まれない」とフラグされた言及単元】
${flagged_mentions}

【推薦理由の全文】
${reason}

【評価の着眼点】
- ルールベース判定では、推薦単元の prerequisite chain (祖先) と入力単元 (理解済み/苦手) のいずれにも該当しない単元名が言及されると「誤った関係」とフラグされる
- しかし、教育的には以下のような言及は妥当な場合がある:
  * 「将来この単元の発展先として◯◯を学ぶ」のような未来言及
  * 「同じ学年の別領域 (例: 数と式 vs 図形) との対比」の言及
  * 「数学全体の中での位置付け」を示す抽象的な言及
- フラグされた言及が、こうした「教育的に妥当な未来言及・横断言及」であれば合格 (score >= 3)
- 一方、文脈と無関係な単元への言及や、前提関係を逆に説明している (例: 「比例は微分の前提です」のような事実誤認) ものは不合格 (score <= 2)

""" + _OUTPUT_FORMAT_INSTRUCTION)


# ===== AI-Q-002: 入力単元への言及 (個別再判定型) =====
INPUT_REFERENCE_JUDGE_PROMPT = Template("""\
あなたは中高生向け数学教育の専門家として、推薦理由が「生徒の入力情報 (学年・理解済み・苦手) を文脈的に踏まえているか」を評価する役割です。

【入力情報】
- 学年: ${grade}
- 理解済み単元: ${understood}
- 苦手単元: ${weak}

【推薦された単元】
- 名前: ${recommended_unit_name}
- 学年区分: ${recommended_unit_grade}

【評価対象の推薦理由】
${reason}

【ルールベース判定の結果】
推薦理由には学年名 / 理解済み単元名 / 苦手単元名のいずれの文字列も明示的には含まれていない。

【評価の着眼点】
- 推薦理由が、文字列としては入力単元名を含まなくても、その学習者の状況を踏まえた内容になっているかを評価する
- 例えば以下のようなケースは「文脈的に踏まえている」と判定して合格 (score >= 3):
  * 入力が「理解済み・苦手とも空 (初学者)」の場合に、その学年の最初に学ぶべき基礎単元から始める旨を述べている
  * 入力単元名そのものではなく、それと等価な表現 (例: 「比例」を「比例関係」と表現) を使っている
  * 学年に応じた難易度・抽象度で書かれている
- 一方、以下は「文脈を踏まえていない」として不合格 (score <= 2):
  * 学年と乖離した内容 (例: 中1生に微分を勧める)
  * 入力情報を完全に無視した汎用的な説明文

""" + _OUTPUT_FORMAT_INSTRUCTION)


# ===== AI-C-003: 一貫性 (同趣旨型) =====
CONSISTENCY_JUDGE_PROMPT = Template("""\
あなたは中高生向け数学教育の専門家として、複数の推薦理由が「同じ趣旨を述べているか」を評価する役割です。

【入力情報 (全ランで共通)】
- 学年: ${grade}
- 理解済み単元: ${understood}
- 苦手単元: ${weak}

【N 回連続で生成された推薦理由 (各ランの全推薦単元の理由を結合した形)】
${all_runs_text}

【ルールベース判定の結果】
全 N 回の推薦理由から抽出された「言及単元の集合」が完全には一致しなかった。
不一致の集合バリエーション数: ${n_unique_sets}

【評価の着眼点】
- 文章表現には揺らぎがあって構わない (生成 AI の自然な揺らぎ)
- 評価すべきは「全回が同じ趣旨か」: 推奨している学習方針・触れている単元概念・教育的なポイントが一貫しているか
- 表記揺れ (例: 「比例」と「比例・反比例」、「方程式」と「一次方程式」) は同一趣旨として扱う
- 一方、以下は「趣旨不一致」として不合格 (score <= 2):
  * ある回は「基礎の復習」を推し、別の回は「応用への発展」を推している
  * 言及している単元が回ごとに大きく異なり、共通項がほぼない

""" + _OUTPUT_FORMAT_INSTRUCTION)


